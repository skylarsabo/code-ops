#!/usr/bin/env node
// Plugin version-bump gate.
//
//   node scripts/check-plugin-bump.mjs --base <ref>
//
// WHY: CLAUDE.md requires that any change under plugins/<name>/ bump that plugin's
// .claude-plugin/plugin.json version and add a plugins/<name>/CHANGELOG.md entry, in the same
// PR. That step is human discipline today, not lint-enforced — lint-plugins.mjs only checks
// that plugin.json and marketplace.json versions stay in parity, never whether a given PR
// actually moved the version. This script closes that gap at the PR-diff level so a plugin
// edit that forgets the version/changelog step fails CI instead of shipping unversioned.
//
// For every top-level plugins/<name>/ path touched between <base> and HEAD, requires BOTH:
//   (a) plugins/<name>/.claude-plugin/plugin.json's "version" differs between `git show
//       <base>:<path>` and the current working-tree file (a plugin.json absent at <base> —
//       i.e. a brand-new plugin — counts as differing)
//   (b) plugins/<name>/CHANGELOG.md appears in the changed-path list
// codex-marketplace/ is a derived-artifact tree (regenerated by build-codex-marketplace.mjs,
// drift-checked in validate.yml): a path under codex-marketplace/plugins/** is never treated as
// a plugins/<name>/ source change, even though it shares the "plugins/<name>/" directory shape.
//
// FAIL-OPEN EXCEPTION (documented, narrow): if <base> does not resolve as a git ref (the initial
// `git diff` errors), this prints a skip note and exits 0 instead of failing the run. This is the
// ONLY fail-open path in this script, and it exists only because push events (as opposed to
// pull_request events) have no meaningful base to diff against. Every other failure mode — bad
// arguments, an unreadable/unparseable plugin.json, a version that did not move, a missing
// changelog entry — is fail-closed.
//
// Exit: 0 = no violations (including the missing-base skip case), 1 = one or more touched
// plugins missing the version bump and/or the changelog entry, 2 = bad invocation (missing/blank
// --base, or an unknown argument).

import { readFileSync, existsSync } from 'node:fs';
import { execFileSync } from 'node:child_process';
import { resolve, dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';

const ROOT = resolve(dirname(fileURLToPath(import.meta.url)), '..');
const GIT_OPTS = { cwd: ROOT, timeout: 10000, maxBuffer: 16 * 1024 * 1024 };

const usage = () => {
  console.error('usage: check-plugin-bump.mjs --base <ref>');
  process.exit(2);
};

// ---------- args ----------
const argv = process.argv.slice(2);
let base = null;
for (let i = 0; i < argv.length; i++) {
  const a = argv[i];
  if (a === '--base') {
    base = argv[++i];
    if (base === undefined) { console.error('x --base needs a ref'); process.exit(2); }
  } else {
    console.error(`x unknown argument: ${a}`);
    usage();
  }
}
if (base === null) { console.error('x --base is required'); usage(); }
if (base.trim() === '') { console.error('x --base must not be blank/whitespace-only'); process.exit(2); }
// A real ref never starts with '-'; rejecting option-like values stops a value from smuggling
// extra git flags even though execFileSync (no shell) already blocks shell-metacharacter injection.
if (base.startsWith('-')) { console.error(`x --base must not look like an option: ${base}`); process.exit(2); }

// ---------- acquire the changed-path list ----------
// -z / NUL-splitting (not the default newline-delimited form) so a path git would otherwise
// C-quote (spaces, non-ASCII, etc.) round-trips exactly — same reasoning as repo-map.mjs's
// `git ls-files -z` use.
let changedPaths;
try {
  const out = execFileSync('git', ['diff', '--name-only', '-z', `${base}...HEAD`, '--'], { ...GIT_OPTS, encoding: 'utf8' });
  changedPaths = out.split('\0').filter(Boolean);
} catch (e) {
  // The ONLY fail-open branch: <base> did not resolve (e.g. a push event with no PR base to
  // diff against, or a typo'd ref). Every other failure below is fail-closed.
  console.log(`OK — base ref '${base}' did not resolve (${String(e.message).split('\n')[0]}) — skipping plugin-bump check (no base to diff against).`);
  process.exit(0);
}

// ---------- group by top-level plugins/<name>/ ----------
// [^/]+ cannot itself contain '/', so a captured name can never smuggle a path-traversal segment.
const PLUGIN_PATH_RE = /^plugins\/([^/]+)\//;
const touched = new Map(); // name -> Set<changed path under that plugin>
for (const p of changedPaths) {
  const m = PLUGIN_PATH_RE.exec(p);
  if (!m) continue; // also excludes codex-marketplace/plugins/** — that tree does not start with "plugins/"
  const name = m[1];
  if (!touched.has(name)) touched.set(name, new Set());
  touched.get(name).add(p);
}

if (touched.size === 0) {
  console.log('OK — no plugins/<name>/ paths changed; nothing to check.');
  process.exit(0);
}

// ---------- helpers ----------
const stripBom = (text) => (text.charCodeAt(0) === 0xFEFF ? text.slice(1) : text);

function readVersion(jsonText, where) {
  let parsed;
  try { parsed = JSON.parse(stripBom(jsonText)); }
  catch (e) { throw new Error(`unparseable JSON at ${where}: ${e.message}`); }
  if (parsed === null || typeof parsed !== 'object' || typeof parsed.version !== 'string' || parsed.version.trim() === '') {
    throw new Error(`missing/blank "version" field at ${where}`);
  }
  return parsed.version;
}

// ---------- per-plugin check ----------
const violations = [];
const clean = [];
for (const name of [...touched.keys()].sort()) {
  const pluginJsonPath = `plugins/${name}/.claude-plugin/plugin.json`;
  const changelogPath = `plugins/${name}/CHANGELOG.md`;
  const reasons = [];

  // (a) version bump — compare <base>'s blob against the current working-tree file.
  let oldVersion = null;
  try {
    const raw = execFileSync('git', ['show', `${base}:${pluginJsonPath}`], { ...GIT_OPTS, encoding: 'utf8' });
    oldVersion = readVersion(raw, `${base}:${pluginJsonPath}`);
  } catch {
    // Absent (or unreadable) at <base> — treated as a brand-new plugin.json, which counts as
    // "differs" below as long as the HEAD version resolves.
    oldVersion = null;
  }

  let newVersion = null;
  const absPluginJson = join(ROOT, ...pluginJsonPath.split('/'));
  if (!existsSync(absPluginJson)) {
    reasons.push(`${pluginJsonPath} does not exist at HEAD (plugin removed, or the path is wrong)`);
  } else {
    try { newVersion = readVersion(readFileSync(absPluginJson, 'utf8'), `HEAD:${pluginJsonPath}`); }
    catch (e) { reasons.push(e.message); }
  }

  if (newVersion !== null && oldVersion !== null && newVersion === oldVersion) {
    reasons.push(`${pluginJsonPath} version unchanged (${oldVersion}) — bump it`);
  }

  // (b) changelog entry — must appear in the changed-path list (content is not inspected; the
  // presence of the path is the contract this gate checks).
  if (!changedPaths.includes(changelogPath)) {
    reasons.push(`${changelogPath} not touched — add a changelog entry`);
  }

  if (reasons.length > 0) violations.push({ name, reasons });
  else clean.push(name);
}

if (violations.length > 0) {
  for (const v of violations) for (const r of v.reasons) console.error(`x ${v.name}: ${r}`);
  console.error(`\n${violations.length} plugin(s) touched without a matching version bump + CHANGELOG.md entry.`);
  process.exit(1);
}

console.log(`OK — every touched plugin bumped its version and added a CHANGELOG.md entry (${clean.join(', ')}).`);
